def daireAlan(r):
    """
    Daire alanını hesaplayıp döndürür.
    (Formül : A = pi * r ** 2)
    pi = 3.14
    """
    pi = 3.14
    result = pi * r ** 2
    print(f"Dairenin alanı : {result}")

def daireCevre(r):
    """
    Daire çevresinin hesaplayıp döndürür.
    (Formül : 2 * pi * r)
    pi = 3.14
    """
    pi = 3.14
    result = 2 * pi * r
    print(f"Dairenin çevresi : {result}")

